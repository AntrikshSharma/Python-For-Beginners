x = int(input('Enter an Integer :'))

if x%2 == 0:     #Checks if the remainder when divided by 2 is 0 or not
                 #True Block
    print('')
    print('The number : ', x, ' is even ')
    
else:           #False Block             
    print('')
    print('The number : ', x, ' is odd ')
    
    
print('Done with conditional')  #Indicates end of program       